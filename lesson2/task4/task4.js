'use strict';

/**
 * Сложение
 * @param a {number}
 * @param b {number}
 * @returns {number}
 */
function customAdd(a, b) {
    return a + b;
}

/**
 * Вычитание
 * @param a {number}
 * @param b {number}
 * @returns {number}
 */
function customSub(a, b) {
    return a - b;
}

/**
 * Деление
 * @param a {number}
 * @param b {number}
 * @returns {number}
 */
function customDiv(a, b) {
    return a / b;
}

/**
 * Умножение
 * @param a {number}
 * @param b {number}
 * @returns {number}
 */
function customMulti(a, b) {
    return a * b;
}